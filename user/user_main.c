void drawPixel(int16_t x, int16_t y, uint16_t color) {
if ((x < 0) || (x >= 128) || (y < 0) || (y >= 64))
return;
// x is which column
switch (color)
{
case 1: pixels[x+ (y/8)*128] |= (1 << (y&7)); break;
case 0: pixels[x+ (y/8)*128] &= ~(1 << (y&7)); break;
case 2: pixels[x+ (y/8)*128] ^= (1 << (y&7)); break;
}
os_printf("Drawpixel at (%d,%d)\n",x,y);
}

void drawBitmap(char *filename) {
 EspFsFile *file = espFsOpen(filename);
 os_printf("Reading bitmap from espfs: '%s'\n",filename);
 int16_t i, j;
 char buffer[16];
 espFsRead(file, buffer, 16); //Skip header
 espFsRead(file, buffer, 16); //...
 espFsRead(file, buffer, 16); //... ugly hack :P
 espFsRead(file, buffer, 14); //...
 for (i=0;i<(64*128/8);i++) {
 pixels[i] = 0x00;
 }
 for (j=0;j<64;j++) {
  os_printf("Line %d\n",j);
  espFsRead(file, buffer, 16);
  for (i=0;i<15;i++) {
   int16_t k = i*8;
   if (buffer[i]&0x01) drawPixel(k,j,1);
   if (buffer[i]&0x02) drawPixel(k+1,j,1);
   if (buffer[i]&0x04) drawPixel(k+2,j,1);
   if (buffer[i]&0x08) drawPixel(k+3,j,1);
   if (buffer[i]&0x10) drawPixel(k+4,j,1);
   if (buffer[i]&0x20) drawPixel(k+5,j,1);
   if (buffer[i]&0x40) drawPixel(k+6,j,1);
   if (buffer[i]&0x80) drawPixel(k+7,j,1);
   os_printf("buffer[%d] = %d\n",i,buffer[i]);
  }
  drawbuffer();
 }
espFsClose(file);
}

